print("Hello, Twitch!")
"""
This script demonstrates various ways to use the print function in Python.
Functions:
  print("Hello, Twitch!"): Prints a greeting message.
  print('Esto tambien funciona con single quote'): Prints a message using single quotes.
  print('Python', 'es', 'genial'): Prints multiple strings separated by a space.
  print('Python', 'es', 'brutal', sep = '-'): Prints multiple strings separated by a hyphen.
  print('Esto se imprime', end = ' '): Prints a message without a newline at the end.
  print('en una linea'): Prints a message on the same line as the previous print statement.
"""
print('Esto tambien funciona con single quote')

print('Python', 'es', 'genial')

print('Python', 'es', 'brutal', sep = '-')

print('Esto se imprime', end = ' ')
print('en una linea')


